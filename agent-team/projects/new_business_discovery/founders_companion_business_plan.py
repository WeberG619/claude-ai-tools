# Simple data models for the platform
from dataclasses import dataclass
from datetime import datetime
from typing import List, Optional
from enum import Enum


class MoodLevel(Enum):
    STRUGGLING = 1
    DIFFICULT = 2
    OKAY = 3
    GOOD = 4
    THRIVING = 5


class ChallengeCategory(Enum):
    FUNDRAISING = "fundraising"
    COFOUNDER = "cofounder_conflict"
    BURNOUT = "burnout"
    LONELINESS = "loneliness"
    IMPOSTER_SYNDROME = "imposter_syndrome"
    WORK_LIFE_BALANCE = "work_life_balance"
    FINANCIAL_STRESS = "financial_stress"
    TEAM_ISSUES = "team_issues"
    PRODUCT_MARKET_FIT = "product_market_fit"
    COMPETITION = "competition"


@dataclass
class FounderProfile:
    """Core founder profile for matching."""
    id: str
    anonymous_name: str  # Used until they choose to reveal
    stage: str  # pre-seed, seed, series-a, etc.
    industry: str
    team_size: int
    years_as_founder: float
    current_challenges: List[ChallengeCategory]
    mood_history: List[tuple[datetime, MoodLevel]]
    vault_entries: int  # Count only, content is encrypted


@dataclass
class VaultEntry:
    """Private confession entry - fully encrypted."""
    id: str
    founder_id: str
    timestamp: datetime
    encrypted_content: bytes  # Only founder can decrypt
    mood_before: MoodLevel
    mood_after: MoodLevel
    ai_response_helpful: Optional[bool]


@dataclass
class FounderMatch:
    """Peer connection between founders."""
    id: str
    founder_1_id: str
    founder_2_id: str
    matched_on: datetime
    common_challenges: List[ChallengeCategory]
    compatibility_score: float
    messages_exchanged: int
    revealed_identity: bool


@dataclass
class DailyCheckIn:
    """Daily mood and status check."""
    id: str
    founder_id: str
    date: datetime
    mood: MoodLevel
    energy_level: int  # 1-10
    biggest_worry: Optional[str]
    small_win: Optional[str]
    ai_response: str
    helpful_rating: Optional[int]


